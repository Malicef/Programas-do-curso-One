public class CharString {

    public static void main(String[] args) {
        char letra = 'a'; //char é declarado com aspas simples '
        System.out.println(letra);

        char valor = 66;
        System.out.println(valor);

        valor = (char) (valor + 1);
        System.out.println(valor);

        String palavra ="SEXO"; //String é declarada com aspas duplas "
        System.out.println(palavra);
    }
}